int		ft_sqrt(int nb)
{
	int		i;
	int		res;

	i = 1;
	res = 0;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	while (res < nb && res >= 0)
	{
		res = i * i;
		if (res == nb)
			return (i);
		i++;
	}
	return (0);
}
