char	*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			i++;
		else if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] += ('a' - 'A');
			i++;
		}
		else
		{
			i++;
		}
	}
	return (str);
}
