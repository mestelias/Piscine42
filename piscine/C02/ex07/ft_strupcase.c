char	*ft_strupcase(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			i++;
		else if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] -= ('a' - 'A');
			i++;
		}
		else
		{
			i++;
		}
	}
	return (str);
}
